#include <unistd.h>

void	ft_print_comb(void);

void	ft_putchar(char i)
{
	write(1, &i, 1);
}

void	print_num(int a, int b, int c)
{
	ft_putchar(a);
	ft_putchar(b);
	ft_putchar(c);
	if (a != '7' || b != '8' || c != '9')
	{
		ft_putchar(',');
		ft_putchar(' ');
	}
}

void	ft_print_comb(void)
{
	char a;
	char b;
	char c;

	a = '0';
	while (a <= '7')
	{
		b = a + 1;
		while (b <= '8')
		{
			c = b + 1;
			while (c <= '9')
			{
				print_num(a, b, c);
				c++;
			}
			b++;
		}
		a++;
	}
}
